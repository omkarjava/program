            package nestedForLoopPatterns;

             public class wap11 {
   
	         public static void main(String[] args) {
	    	  int r=5;
	    	  
	 		 for(int i=r;i>=1;i--)
	 		 {
	 		 char ch='A';
	 		 for(int j=i;j>=1;j--)
	 		 {
	 		 System.out.print(ch);
	 		 ch++;
	 		 }
	 		 System.out.println(); 
	           
	 		 }
	 		
	 		 for(int i=1;i<=r;i++)
	 		 {
	 		  char ba='A';
	 		 
	 		for(int j=1;j<=i;j++) 
	 		{
	 			System.out.print(ba);
		 		 ba++;	
	 		 }
	 		 System.out.println();
	 		 }
	         }}

	


