        package practice;

        public class loop {

	    public static void main(String[] args) {
		int a=5,i;
		for( i=1;i<=a;i++);
		{
			
			for(int j=1;j<=a-i;j++)
			{
			System.out.print(" ");
			}
			for(int k=1;k<=i;k++)
			{
				System.out.print(k);
			}
			System.out.println();
		}//System.out.println();
		

	}

}
