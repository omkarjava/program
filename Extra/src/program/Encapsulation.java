package program;

public class Encapsulation {
	private int a=5;
	private String name="om";
	
	public int getA() {
		return a;
	}
	public void setA(int a) {
		this.a = a;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	

}
  
